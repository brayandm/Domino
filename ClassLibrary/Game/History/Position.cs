// Este enum representa los tipos de movimientos
public enum Position 
{
    // Este valor indica que se jugo un token por la izquierda
    Left,
    // Este valor indica que se jugo el primer token
    Middle,
    // Este valor indica que se jugo un token por la derecha
    Right,
    // Este valor indica que el jugador se paso de turno
    Pass,
    // Este valor indica que el jugador robo una ficha
    Draw,
}